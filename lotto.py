# 로또
import random
lotto=[]; num=0
result=True
print("===로또 추첨===")
while result:
    num=random.randrange(1,46)
    if lotto.count(num)==0:
       lotto.append(num)
    if len(lotto)>=6:
        result=False
else:
    print("추첨된 번호" , end="\n")
    lotto.sort()
for i in range(0,len(lotto)):
    print("{}".format(lotto[i],end=""))

